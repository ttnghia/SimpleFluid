#include "Simulator.h"

//-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+
void Simulator::setParticleData(const std::shared_ptr<ParticleSystemData>& particleData)
{
    m_ParticleData = particleData;
}

const std::shared_ptr<ParticleSystemData>& Simulator::getParticleDataObj()
{
    return m_ParticleData;
}

//-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+
void Simulator::startSimulation(const std::shared_ptr<SimulationParameters>& simParams)
{
    m_SimParams = simParams;
    QMetaObject::invokeMethod(this, "doSimulation", Qt::QueuedConnection); // run in parallel
}

